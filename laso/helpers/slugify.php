<?php
if (!function_exists('slugify')) {
   function slugify($text)
{
    // Bảng thay thế các ký tự có dấu
    $vietnameseChars = [
        'a' => 'á|à|ả|ã|ạ|ă|ắ|ằ|ẳ|ẵ|ặ|â|ấ|ầ|ẩ|ẫ|ậ',
        'd' => 'đ',
        'e' => 'é|è|ẻ|ẽ|ẹ|ê|ế|ề|ể|ễ|ệ',
        'i' => 'í|ì|ỉ|ĩ|ị',
        'o' => 'ó|ò|ỏ|õ|ọ|ô|ố|ồ|ổ|ỗ|ộ|ơ|ớ|ờ|ở|ỡ|ợ',
        'u' => 'ú|ù|ủ|ũ|ụ|ư|ứ|ừ|ử|ữ|ự',
        'y' => 'ý|ỳ|ỷ|ỹ|ỵ',
        'A' => 'Á|À|Ả|Ã|Ạ|Ă|Ắ|Ằ|Ẳ|Ẵ|Ặ|Â|Ấ|Ầ|Ẩ|Ẫ|Ậ',
        'D' => 'Đ',
        'E' => 'É|È|Ẻ|Ẽ|Ẹ|Ê|Ế|Ề|Ể|Ễ|Ệ',
        'I' => 'Í|Ì|Ỉ|Ĩ|Ị',
        'O' => 'Ó|Ò|Ỏ|Õ|Ọ|Ô|Ố|Ồ|Ổ|Ỗ|Ộ|Ơ|Ớ|Ờ|Ở|Ỡ|Ợ',
        'U' => 'Ú|Ù|Ủ|Ũ|Ụ|Ư|Ứ|Ừ|Ử|Ữ|Ự',
        'Y' => 'Ý|Ỳ|Ỷ|Ỹ|Ỵ',
    ];

    foreach ($vietnameseChars as $replacement => $search) {
        $text = preg_replace("/($search)/i", $replacement, $text);
    }

    // Chuyển sang chữ thường
    $text = strtolower($text);
    // Xóa các ký tự không phải chữ, số, gạch ngang
    $text = preg_replace('/[^a-z0-9 -]+/', '', $text);
    // Thay thế khoảng trắng và các ký tự gạch ngang liên tiếp bằng một gạch ngang duy nhất
    $text = preg_replace('/[\s-]+/', '-', $text);
    // Xóa gạch ngang ở đầu và cuối chuỗi
    $text = trim($text, '-');

    return $text;
}

}
